    private void btnDoneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDoneActionPerformed
        // TODO add your handling code here:
         String choosenAns=getUserAnswer();
        if(choosenAns!=null){
         QuestionPojo quest=qs.getQuestionByQno(qno);
         String correctAns=quest.getCorrect_ans();
         AnswerPojo ans=new AnswerPojo();
         ans.setChoosenAnswer(choosenAns);
         ans.setCorrectAnswer(correctAns);
         ans.setQno(qno);
         ans.setExamid(exam.getExamID());
         ans.setSubject(exam.getLanguage());
         AnswerPojo attemped=astore.getAnswerByQno(qno);
         if(attemped==null){
             astore.addAnswer(ans);
         }
          else{
             String previousAns=attemped.getChoosenAnswer();
             if(choosenAns.equalsIgnoreCase(previousAns)==false){
                int apos=astore.removeAnswer(ans);
                astore.setAnswer(apos, ans);
             }
        }
        }
        int right=0,wrong=0,unattempted=0;
        for(QuestionPojo q:qs.getAllQuestion()){
            int qno=q.getQno();
            AnswerPojo ans=astore.getAnswerByQno(qno);
            System.out.println("ans is "+ans);
            if(ans==null){
                ++unattempted;
            }
            else{
            String attemptedAns=ans.getChoosenAnswer();
            String correctAns=ans.getCorrectAnswer();
            if(attemptedAns.equals(correctAns)){
                ++right;
            }
            else{
                ++wrong;
            }
            }
        }
        StringBuilder sb=new StringBuilder();
        sb.append("right ans:"+right);
        sb.append("wrong ans:"+wrong);
        sb.append("unattempted ans:"+unattempted);
        JOptionPane.showMessageDialog(null,sb.toString(),"result",JOptionPane.INFORMATION_MESSAGE);
        try{
            PerformancePojo performance=new PerformancePojo();
            performance.setExamid(exam.getExamID());
            performance.setLanguage(exam.getLanguage());
            performance.setUserid(UserProfile.getUserId());
            performance.setRight(right);
            performance.setUnattemp(unattempted);
            performance.setWrong(wrong);
            performance.setPer((double)right/qs.getCount()*100);
            PerformanceDAO.addPerformance(performance);
          JOptionPane.showMessageDialog(null,"result Saved thanku for the test","done",JOptionPane.INFORMATION_MESSAGE);
          PaperSelectionFrame f=new PaperSelectionFrame();
          f.setVisible(true);
          this.dispose();
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"problem in db","Error",JOptionPane.ERROR_MESSAGE);
            System.out.println(ex);

        }
    }//GEN-LAST:event_btnDoneActionPerformed

