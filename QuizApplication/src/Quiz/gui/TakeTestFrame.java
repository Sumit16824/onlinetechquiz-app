    private void btnNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNextActionPerformed
        // TODO add your handling code here:
        String choosenAns=getUserAnswer();
        if(choosenAns!=null){
         QuestionPojo quest=qs.getQuestionByQno(qno);
         String correctAns=quest.getCorrect_ans();
         AnswerPojo ans=new AnswerPojo();
         ans.setChoosenAnswer(choosenAns);
         ans.setCorrectAnswer(correctAns);
         ans.setQno(qno);
         ans.setExamid(exam.getExamID());
         ans.setSubject(exam.getLanguage());
         AnswerPojo attemped=astore.getAnswerByQno(qno);
         if(attemped==null){
             astore.addAnswer(ans);
         }
         else{
             String previousAns=attemped.getChoosenAnswer();
             if(choosenAns.equalsIgnoreCase(previousAns)==false){
                int apos=astore.removeAnswer(ans);
                astore.setAnswer(apos, ans);
             }
         }
        }
         pos++;
           if(pos>=qs.getCount()){
            pos=0;
           }
           
           qno++;
             if(qno>qs.getCount()){
                 qno=1;
           }
        lblQno.setText("Question no :"+qno);
        showQuestions();
    }//GEN-LAST:event_btnNextActionPerformed

